
age = int(input("What is your current age?"))


yearsLeft = 90 - age
weeksLeft = yearsLeft * 52
daysLeft = yearsLeft * 365
hoursLeft = daysLeft * 24


print(f"You have {weeksLeft} weeks left on Earth. Enjoy.")

print(f"You have {daysLeft} days left on Earth. Enjoy.")

print(f"You have {hoursLeft} hours left on Earth. Enjoy.")
